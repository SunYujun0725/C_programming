#include <stdio.h>

int main()
{
    int a,d,e,i,j;
    scanf("%d",&a);
    int b[a];
    int c[a];
    for(int i=0;i<a;i++){
        scanf("%d",&d);
        b[i]=d;
    }
    for(int j=0;j<a;j++){
        scanf("%d",&e);
        c[j]=e;
    }
    i=0;
    j=0;
    while(i<a){
        printf("%d",b[i]);
        printf(" ");
        while(j<a){
            printf("%d",c[j]);
            printf(" ");
            j++;
            break;
        }
        i++;
    }
    return 0;
}
