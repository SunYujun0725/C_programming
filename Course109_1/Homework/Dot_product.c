#include <stdio.h>
#include <math.h>

int main()
{
    int x,y,z,a,b,c,d,e,f;
    float g,h,i,j,k,l,m,n;
    scanf("%d %d %d",&x,&y,&z);
    scanf("%d %d %d",&a,&b,&c);
    d=y*c-b*z;
    e=z*a-x*c;
    f=x*b-a*y;
    m=d*d+e*e+f*f;
    double root;
    n=root=sqrt(m);
    g=d/n;
    h=e/n;
    i=f/n;
    printf("%.4f\t",g);
    printf("%.4f\t",h);
    printf("%.4f\t",i);
    j=-g;
    k=-h;
    l=-i;
    printf("\n");
    printf("%.4f\t",j);
    printf("%.4f\t",k);
    printf("%.4f\n",l);
    
    return 0;
}
