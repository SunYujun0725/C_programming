#include <stdio.h>
#include <string.h>
int main()
{
    char str[2000000];
    fgets (str, 2000000, stdin);
     int m = 0;
        while (*(str + m) != '\0')  //找字串長度
        {
          m++;
        }
      for(int i=0;i<m;i++){
            if(str[i]==' '||str[i]=='/'||str[i]==','){
                printf("\n");
                
            }
            else{
                printf("%c",str[i]);
            }
        
    }
    return 0;
}
