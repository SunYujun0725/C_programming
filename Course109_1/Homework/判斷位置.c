#include <stdio.h>

int main()
{
    float a,b,c,d,e,f,g,h,i,j,m;
      
       scanf("%f %f",&a,&b);
       scanf("%f %f",&c,&d);
       scanf("%f %f",&e,&f);
       
       g=c-a;
       h=d-b;
       i=e-a;
       j=f-b;
       m=g*j-h*i;
       if (m>0)
       {
           printf("LEFT\n");
           
       }
       else if (m<0)
       {
           printf("RIGHT\n");
           
       }
       else
       {
           printf("GREAT\n");
       }
    return 0;
}
