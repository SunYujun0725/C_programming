#include <stdio.h>

int main()
{
    int n;
    scanf("%d",&n);
    int a[n];
    for(int i=0;i<n;i++){
        scanf("%d",&a[i]);
    }
    int b[n];
    int c[n];
    int x=0;
    int y=0;
    int j=0;
    int k=0;
    int i=0;
    while(i<n){
      if(a[i]%2==0){
          b[j]=a[i];
                x++;
                j++;
                
            
        }
       else{
                c[k]=a[i];
                y++;
                k++;
               
        }
        i++;
    }

    
    for(int k=0;k<y;k++){
        printf("%d",c[k]);
        printf(" ");
    }
    printf("\n");
    for(int j=0;j<x;j++){
        printf("%d",b[j]);
        printf(" ");
    }
    return 0;
}
