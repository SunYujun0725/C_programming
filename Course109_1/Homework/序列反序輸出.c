#include <stdio.h>

int main()
{
    int a,i,j;
    scanf("%d",&a);
    int b [a];
    for(i=0;i<a;i++){
        scanf("%d",&j);
        b[i]=j;
    }
   
   for(i=i-1;i>0;i--){
       printf("%d",b[i]);
       printf(" ");
   }
    printf("%d\n",b[i]);
    
    return 0;
}
